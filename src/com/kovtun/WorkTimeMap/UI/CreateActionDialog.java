    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        
        if (!nameTextField.getText().isEmpty()&&!descriptionArea.getText().isEmpty()){
            if(action==null){
               action=new Action();
               action.setName(nameTextField.getText());
               action.setDescription(descriptionArea.getText());
               action.setComment(CommentArea.getText());
               action.setUserId(Values.user);
               action.setProjectId(projects.get(jComboBox1.getSelectedIndex()));
               action.setStartTime(Date.from(dateTimePicker1.getDateTimePermissive().atZone(ZoneId.systemDefault()).toInstant()));
               if(jCheckBox1.isSelected())
                    action.setStopTime(Date.from(dateTimePicker2.getDateTimePermissive().atZone(ZoneId.systemDefault()).toInstant()));
               callback.createAction(action);
               dispose();
            }else{
               action.setName(nameTextField.getText());
               action.setDescription(descriptionArea.getText());
               action.setComment(CommentArea.getText());
               action.setUserId(Values.user);
               action.setProjectId(projects.get(jComboBox1.getSelectedIndex()));
               action.setStartTime(Date.from(dateTimePicker1.getDateTimePermissive().atZone(ZoneId.systemDefault()).toInstant()));
               if(jCheckBox1.isSelected())
                    action.setStopTime(Date.from(dateTimePicker2.getDateTimePermissive().atZone(ZoneId.systemDefault()).toInstant()));
               else
                   action.setStopTime(null);
               callback.updateAction(action);
               dispose();
            }
        }else {
            JOptionPane.showMessageDialog(this, "Не все поля заполнены","Ошибка", JOptionPane.PLAIN_MESSAGE);

        }
        
    }//GEN-LAST:event_okButtonActionPerformed

