    private void setDataToView() {
        for(int i=0;i<projects.size();i++)
            if(projects.get(i).getId()==action.getProjectId().getId())
                jComboBox1.setSelectedIndex(i);

        nameTextField.setText(action.getName());
        descriptionArea.setText(action.getDescription());
        CommentArea.setText(action.getComment());
        Calendar calendar=new Calendar.Builder().setInstant(action.getStartTime()).build();
        dateTimePicker1.setDateTimePermissive(LocalDateTime.ofInstant(action.getStartTime().toInstant(), ZoneId.systemDefault()));
        if(action.getStopTime()!=null)
            dateTimePicker2.setDateTimePermissive(LocalDateTime.ofInstant(action.getStopTime().toInstant(), ZoneId.systemDefault()));
        //startTimeLabel.setText(new SimpleDateFormat("dd.MM.yyyy HH:mm").format(action.getStartTime()));
        //stopTimeLabel.setText(action.getStopTime()!=null?new SimpleDateFormat("dd.MM.yyyy HH:mm").format(action.getStopTime()):"Еще не завершено!");
        
    }
