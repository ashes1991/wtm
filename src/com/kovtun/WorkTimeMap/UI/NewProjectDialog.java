    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Project project=new Project();
        project.setName(jTextField1.getText());
        project.setCreatorId(Values.user);
        project.setStartTime(dateChooserCombo1.getCurrent().getTime());
        List<User> uList=new ArrayList<>();
        for(int index:jList1.getSelectedIndices())
            uList.add(userList.get(index));
        project.setUserList(uList);
        callback.addNewproject(project);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    

