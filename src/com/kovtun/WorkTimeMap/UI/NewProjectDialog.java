    private void checkToEnableOkButton(){
        if(jList1.getSelectedIndex()!=-1)
            if(!jTextField1.getText().isEmpty()&&jTextField1.getText().length()>3)
                jButton1.setEnabled(true);
            else
                jButton1.setEnabled(false);
        else
            jButton1.setEnabled(false);
    }
